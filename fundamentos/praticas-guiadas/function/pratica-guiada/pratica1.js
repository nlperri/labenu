function pratica1Normal(nome) {
  return `Olá, ${nome}`;
}

const pratica1Arrow = (nome) => {
  return `Olá, ${nome}`;
};

const pratica1Exp = function (nome) {
  return `Olá, ${nome}`;
};

console.log(pratica1Normal("João"));

console.log(pratica1Arrow("Maria"));

